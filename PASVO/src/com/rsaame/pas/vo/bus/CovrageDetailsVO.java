package com.rsaame.pas.vo.bus;

import com.mindtree.ruc.cmn.base.BaseVO;

/**
 * 
 * @author m1014438
 *
 */
public class CovrageDetailsVO extends BaseVO{
	private char vehiclePABD;
	private char vehiclePABP;
	private char vehicleLossofuse;
	private char vehicleGeographicalExtension;
	private char[] vehicleGeographicalPlace;

	/**
	 * @return the vehiclePABD
	 */
	public char getVehiclePABD(){
		return vehiclePABD;
	}

	/**
	 * @param vehiclePABD the vehiclePABD to set
	 */
	public void setVehiclePABD( char vehiclePABD ){
		this.vehiclePABD = vehiclePABD;
	}

	/**
	 * @return the vehiclePABP
	 */
	public char getVehiclePABP(){
		return vehiclePABP;
	}

	/**
	 * @param vehiclePABP the vehiclePABP to set
	 */
	public void setVehiclePABP( char vehiclePABP ){
		this.vehiclePABP = vehiclePABP;
	}

	/**
	 * @return the vehicleLossofuse
	 */
	public char getVehicleLossofuse(){
		return vehicleLossofuse;
	}

	/**
	 * @param vehicleLossofuse the vehicleLossofuse to set
	 */
	public void setVehicleLossofuse( char vehicleLossofuse ){
		this.vehicleLossofuse = vehicleLossofuse;
	}

	/**
	 * @return the vehicleGeographicalExtension
	 */
	public char getVehicleGeographicalExtension(){
		return vehicleGeographicalExtension;
	}

	/**
	 * @param vehicleGeographicalExtension the vehicleGeographicalExtension to set
	 */
	public void setVehicleGeographicalExtension( char vehicleGeographicalExtension ){
		this.vehicleGeographicalExtension = vehicleGeographicalExtension;
	}

	/**
	 * @return the vehicleGeographicalPlace
	 */
	public char[] getVehicleGeographicalPlace(){
		return vehicleGeographicalPlace;
	}

	/**
	 * @param vehicleGeographicalPlace the vehicleGeographicalPlace to set
	 */
	public void setVehicleGeographicalPlace( char[] vehicleGeographicalPlace ){
		this.vehicleGeographicalPlace = vehicleGeographicalPlace;
	}

	@Override
	public Object getFieldValue( String fieldName ){

		Object fieldValue = null;
		if( "vehiclePABD".equals( fieldName ) ) fieldValue = getVehiclePABD();
		if( "vehiclePABP".equals( fieldName ) ) fieldValue = getVehiclePABP();
		if( "vehicleLossofuse".equals( fieldName ) ) fieldValue = getVehicleLossofuse();
		if( "vehicleGeographicalExtension".equals( fieldName ) ) fieldValue = getVehicleGeographicalExtension();
		if( "vehicleGeographicalPlace".equals( fieldName ) ) fieldValue = getVehicleGeographicalPlace();
		return fieldValue;
	}

}
