package com.rsaame.pas.vo.bus;


public class MBUWDetailsVO extends UWDetails {

	
	private SimpleUWDetailsVO simpleUWDetailsVO;
	private Double emlPrc;
	private Double emlBIPrc; 
	private Double emlBI;
	private Double emlSI; 
	private Integer categoryRI;
	
	@Override
	public Object getFieldValue(String fieldName) {
		
		Object fieldValue=null;
		
		if( "emlPrc".equals( fieldName ) ) fieldValue = getEmlPrc();
		if( "emlBIPrc".equals( fieldName ) ) fieldValue = getEmlBIPrc();
		if( "emlBI".equals( fieldName ) ) fieldValue = getEmlBI();
		if( "emlSI".equals( fieldName ) ) fieldValue = getEmlSI();
		if( "categoryRI".equals( fieldName ) ) fieldValue = getCategoryRI();
		
		return fieldValue;
	}

	public SimpleUWDetailsVO getSimpleUWDetailsVO() {
		return simpleUWDetailsVO;
	}

	public void setSimpleUWDetailsVO(SimpleUWDetailsVO simpleUWDetailsVO) {
		this.simpleUWDetailsVO = simpleUWDetailsVO;
	}

	public Double getEmlPrc() {
		return emlPrc;
	}

	public void setEmlPrc(Double emlPrc) {
		this.emlPrc = emlPrc;
	}

	public Double getEmlBIPrc() {
		return emlBIPrc;
	}

	public void setEmlBIPrc(Double emlBIPrc) {
		this.emlBIPrc = emlBIPrc;
	}

	public Double getEmlBI() {
		return emlBI;
	}

	public void setEmlBI(Double emlBI) {
		this.emlBI = emlBI;
	}

	public Double getEmlSI() {
		return emlSI;
	}

	public void setEmlSI(Double emlSI) {
		this.emlSI = emlSI;
	}

	public Integer getCategoryRI() {
		return categoryRI;
	}

	public void setCategoryRI(Integer categoryRI) {
		this.categoryRI = categoryRI;
	}

}
