package com.rsaame.pas.vo.bus;

import com.mindtree.ruc.cmn.base.BaseVO;
/**
 * 
 * @author m1014438
 *
 */
public class VehicleDetailsVO extends BaseVO{

	private String vehicleYearOfMake;
	private String vehicleModel;
	private Integer vehicleCategory;
	private String vehicleYearofMfg;//kindly finalize it should be either string or date
	private Integer vehicleIEV;
	private Integer vehicleExcess;
	private String vehicleDealerOption;
	private Integer vehicleSeatCapacity;

	/**
	 * @return the vehicleYearOfMake
	 */
	public String getVehicleYearOfMake(){
		return vehicleYearOfMake;
	}

	/**
	 * @param vehicleYearOfMake the vehicleYearOfMake to set
	 */
	public void setVehicleYearOfMake( String vehicleYearOfMake ){
		this.vehicleYearOfMake = vehicleYearOfMake;
	}

	/**
	 * @return the vehicleModel
	 */
	public String getVehicleModel(){
		return vehicleModel;
	}

	/**
	 * @param vehicleModel the vehicleModel to set
	 */
	public void setVehicleModel( String vehicleModel ){
		this.vehicleModel = vehicleModel;
	}

	/**
	 * @return the vehicleCategory
	 */
	public Integer getVehicleCategory(){
		return vehicleCategory;
	}

	/**
	 * @param vehicleCategory the vehicleCategory to set
	 */
	public void setVehicleCategory( Integer vehicleCategory ){
		this.vehicleCategory = vehicleCategory;
	}

	/**
	 * @return the vehicleYearofMfg
	 */
	public String getVehicleYearofMfg(){
		return vehicleYearofMfg;
	}

	/**
	 * @param vehicleYearofMfg the vehicleYearofMfg to set
	 */
	public void setVehicleYearofMfg( String vehicleYearofMfg ){
		this.vehicleYearofMfg = vehicleYearofMfg;
	}

	/**
	 * @return the vehicleIEV
	 */
	public Integer getVehicleIEV(){
		return vehicleIEV;
	}

	/**
	 * @param vehicleIEV the vehicleIEV to set
	 */
	public void setVehicleIEV( Integer vehicleIEV ){
		this.vehicleIEV = vehicleIEV;
	}

	/**
	 * @return the vehicleExcess
	 */
	public Integer getVehicleExcess(){
		return vehicleExcess;
	}

	/**
	 * @param vehicleExcess the vehicleExcess to set
	 */
	public void setVehicleExcess( Integer vehicleExcess ){
		this.vehicleExcess = vehicleExcess;
	}

	/**
	 * @return the vehicleDealerOption
	 */
	public String getVehicleDealerOption(){
		return vehicleDealerOption;
	}

	/**
	 * @param vehicleDealerOption the vehicleDealerOption to set
	 */
	public void setVehicleDealerOption( String vehicleDealerOption ){
		this.vehicleDealerOption = vehicleDealerOption;
	}

	/**
	 * @return the vehicleSeatCapacity
	 */
	public Integer getVehicleSeatCapacity(){
		return vehicleSeatCapacity;
	}

	/**
	 * @param vehicleSeatCapacity the vehicleSeatCapacity to set
	 */
	public void setVehicleSeatCapacity( Integer vehicleSeatCapacity ){
		this.vehicleSeatCapacity = vehicleSeatCapacity;
	}

	@Override
	public Object getFieldValue( String fieldName ){
		Object fieldValue = null;
		if( "vehicleYearOfMake".equals( fieldName ) ) fieldValue = getVehicleYearOfMake();
		if( "vehicleModel".equals( fieldName ) ) fieldValue = getVehicleModel();
		if( "vehicleCategory".equals( fieldName ) ) fieldValue = getVehicleCategory();
		if( "vehicleYearofMfg".equals( fieldName ) ) fieldValue = getVehicleYearofMfg();
		if( "vehicleIEV".equals( fieldName ) ) fieldValue = getVehicleIEV();
		if( "vehicleExcess".equals( fieldName ) ) fieldValue = getVehicleExcess();
		if( "vehicleDealerOption".equals( fieldName ) ) fieldValue = getVehicleDealerOption();
		if( "vehicleSeatCapacity".equals( fieldName ) ) fieldValue = getVehicleSeatCapacity();

		return fieldValue;
	}

}
