package com.rsaame.pas.vo.bus;

public class EEVO extends RiskGroupDetails {
	/**
	 * 
	 */

	private java.util.List<EquipmentVO> equipmentDtls = new com.mindtree.ruc.cmn.utils.List<EquipmentVO>(EquipmentVO.class);
	private Integer hazardLevel;

	public java.util.List<EquipmentVO> getEquipmentDtls() {
		return equipmentDtls;
	}

	public void setEquipmentDtls(java.util.List<EquipmentVO> equipmentDtls) {
		this.equipmentDtls = equipmentDtls;
	}

	public Object getFieldValue( String fieldName ){
		Object fieldValue = null;

		if( "equipmentDtls".equals( fieldName ) ) fieldValue = getEquipmentDtls();
		
		return fieldValue;
	}
	
	@Override
	public String toString() {
		return "EEVO [EEDets=" + equipmentDtls + "]";
	}

	public Integer getHazardLevel() {
		return hazardLevel;
	}

	public void setHazardLevel(Integer hazardLevel) {
		this.hazardLevel = hazardLevel;
	}
	
	

}
