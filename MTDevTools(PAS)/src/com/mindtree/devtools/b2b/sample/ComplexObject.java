package com.mindtree.devtools.b2b.sample;

import java.io.Serializable;
import java.util.Date;
import java.util.List;

/**
 * @author SMurthy
 *
 */
public class ComplexObject implements Serializable{

	private static final long serialVersionUID = 1L;

	private BeanB beanB;

	private List<BeanC> beanCList;

	private Date date;

	private Object object;

	private String string;

	private List<String> stringList;

	public BeanB getBeanB(){
		return beanB;
	}

	/**
	 * @return the beanCList
	 */
	public List<BeanC> getBeanCList(){
		return beanCList;
	}

	/**
	 * @return the date
	 */
	public Date getDate(){
		return date;
	}

	/**
	 * @return the object
	 */
	public Object getObject(){
		return object;
	}

	/**
	 * @return the string
	 */
	public String getString(){
		return string;
	}

	/**
	 * @return the stringList
	 */
	public List<String> getStringList(){
		return stringList;
	}

	public void setBeanB( BeanB beanB ){
		this.beanB = beanB;
	}

	/**
	 * @param beanCList the beanCList to set
	 */
	public void setBeanCList( List<BeanC> beanCList ){
		this.beanCList = beanCList;
	}

	/**
	 * @param date the date to set
	 */
	public void setDate( Date date ){
		this.date = date;
	}

	/**
	 * @param object the object to set
	 */
	public void setObject( Object object ){
		this.object = object;
	}

	/**
	 * @param string the string to set
	 */
	public void setString( String string ){
		this.string = string;
	}

	/**
	 * @param stringList the stringList to set
	 */
	public void setStringList( List<String> stringList ){
		this.stringList = stringList;
	}

}
