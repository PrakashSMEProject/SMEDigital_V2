package com.rsaame.pas.b2b.ws.validators;

import java.util.List;

import org.codehaus.jackson.annotate.JsonProperty;

import com.rsaame.pas.b2b.ws.validators.BusinessValidators;
import com.rsaame.pas.b2b.ws.validators.SchemaValidators;
import com.rsaame.pas.b2b.ws.validators.SecurityValidators;

public class SBSWSValidations {
	@JsonProperty("BusinessValidators")
	private List<BusinessValidators> businessValidators;
	@JsonProperty("SecurityValidators")
	private List<SecurityValidators> securityValidators;
	@JsonProperty("BusinessValidators")
	private List<SchemaValidators> SchemaValidators;

	public List<BusinessValidators> getBusinessValidators() {
		return businessValidators;
	}

	public	void setBusinessValidators(List<BusinessValidators> businessValidators) {
		this.businessValidators = businessValidators;
	}

	public List<SecurityValidators> getSecurityValidators() {
		return securityValidators;
	}

	public void setSecurityValidators(List<SecurityValidators> securityValidators) {
		this.securityValidators = securityValidators;
	}

	public List<SchemaValidators> getSchemaValidators() {
		return SchemaValidators;
	}

	public void setSchemaValidators(List<SchemaValidators> schemaValidators) {
		SchemaValidators = schemaValidators;
	}

}
