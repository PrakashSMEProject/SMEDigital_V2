package com.rsaame.pas.b2c.ws.beans;

public class SourceOfBusiness {

	private Integer distributionChannel;
	private Integer brokerName;
	private Long directSubAgent;
	private String customerSource;
	private String promoCode;
	
	public Integer getDistributionChannel() {
		return distributionChannel;
	}
	public void setDistributionChannel(Integer distributionChannel) {
		this.distributionChannel = distributionChannel;
	}
	public Integer getBrokerName() {
		return brokerName;
	}
	public void setBrokerName(Integer brokerName) {
		this.brokerName = brokerName;
	}
	public Long getDirectSubAgent() {
		return directSubAgent;
	}
	public void setDirectSubAgent(Long directSubAgent) {
		this.directSubAgent = directSubAgent;
	}
	public String getCustomerSource() {
		return customerSource;
	}
	public void setCustomerSource(String customerSource) {
		this.customerSource = customerSource;
	}
	public String getPromoCode() {
		return promoCode;
	}
	public void setPromoCode(String promoCode) {
		this.promoCode = promoCode;
	}
	
}
