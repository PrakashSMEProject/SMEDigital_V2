package com.rsaame.pas.b2c.ws.vo;

import java.util.Date;

import org.codehaus.jackson.annotate.JsonProperty;

public class TravelDetails {
	@JsonProperty("InclUSACa")
	Boolean inclUSACa;
	@JsonProperty("EffectiveDate")
	Date startDate;	
	@JsonProperty("ExpiryDate")
	Date endDate;
	@JsonProperty("Travelers")
	Travellers[] travelers;
	
	public Boolean getInclUSACa() {
		return inclUSACa;
	}
	public void setInclUSACa(Boolean inclUSACa) {
		this.inclUSACa = inclUSACa;
	}
	public Date getStartDate() {
		return startDate;
	}
	public void setStartDate(Date startDate) {
		this.startDate = startDate;
	}
	public Date getEndDate() {
		return endDate;
	}
	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}
	public Travellers[] getTravelers() {
		return travelers;
	}
	public void setTravelers(Travellers[] travelers) {
		this.travelers = travelers;
	}

}
